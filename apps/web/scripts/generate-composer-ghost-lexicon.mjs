import { createRequire } from "node:module";
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const require = createRequire(import.meta.url);
const sourcePath = require.resolve("subtlex-word-frequencies");
const source = JSON.parse(readFileSync(sourcePath, "utf8"));

const MAX_WORDS = 35000;
const DOMAIN_WORDS = [
  "Prism",
  "Ollama",
  "Qdrant",
  "OpenAI",
  "LocalAI",
  "SQLite",
  "Docker",
  "NextJS",
  "TypeScript",
  "miscellaneous",
];

const seen = new Set();
const words = [];

function addWord(word) {
  if (typeof word !== "string") return;
  if (!/^[a-z]+$/iu.test(word)) return;
  if (word.length < 5 || word.length > 24) return;
  const key = word.toLowerCase();
  if (seen.has(key)) return;
  seen.add(key);
  words.push(word);
}

DOMAIN_WORDS.forEach(addWord);

for (const entry of source) {
  if (words.length >= MAX_WORDS) break;
  addWord(entry?.word);
}

const targetPath = join(
  dirname(fileURLToPath(import.meta.url)),
  "..",
  "src",
  "app",
  "composerGhostLexicon.generated.ts"
);

const body = words.map((word) => `  ${JSON.stringify(word)},`).join("\n");
const content = `// Generated by apps/web/scripts/generate-composer-ghost-lexicon.mjs.
// Source: subtlex-word-frequencies, filtered to the most common local words.

export const COMPOSER_GHOST_LEXICON: readonly string[] = [
${body}
];
`;

writeFileSync(targetPath, content, "utf8");
console.log(`Wrote ${words.length} words to ${targetPath}`);
